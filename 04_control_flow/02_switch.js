// Switch

let weekDays = "a";
switch (weekDays) {
  case "a":
    console.log("Monday");
    break;
  case "b":
    console.log("Tuesday");
    break;
  case "c":
    console.log("Wednesday");
    break;
  case "d":
    console.log("Thursday");
    break;
  case "e":
    console.log("Friday");
    break;
  case "f":
    console.log("Saturday");
    break;
  case "g":
    console.log("Sunday");
    break;
  default:
    console.log("8th days is not in week days");
    break;
}