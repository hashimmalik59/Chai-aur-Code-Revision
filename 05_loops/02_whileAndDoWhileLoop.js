// While and Do while loop

// while - loops through a block of code while a specified condition is true

let i = 0;
while (i <= 10) {
    // console.log(i);
    i++;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// do while loop creates a loop that executes a specified statement as long as the test condition evaluates to true
// (it will run at least once wether the condition true of false)

let index = 100; // executes at least once
do {
    // console.log(index);
    index++;
} while (index <= 10);